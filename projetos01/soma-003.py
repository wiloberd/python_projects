# Programa que leia dois números e mostre a soma entre eles.

print('''Este programa leia dois números
e mostre a soma entre eles.''')

first_number = int(input('Digite o primeiro numero: '))
second_number = int(input('Digite o segundo numero: '))
soma = first_number + second_number

print(f'A soma entre {first_number} e {second_number} vale: {soma}')